package com.fox.exercise.api.entity;

public class UserGift {
    private int uid;
    private String giftPic;
    private String giftName;
    private int giftCharm;
    private int giftRich;
    private long addTime;
    private String sendName;

    public String getSendName() {
        return sendName;
    }

    public void setSendName(String sendName) {
        this.sendName = sendName;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getGiftPic() {
        return giftPic;
    }

    public void setGiftPic(String giftPic) {
        this.giftPic = giftPic;
    }

    public String getGiftName() {
        return giftName;
    }

    public void setGiftName(String giftName) {
        this.giftName = giftName;
    }

    public long getAddTime() {
        return addTime;
    }

    public void setAddTime(long addTime) {
        this.addTime = addTime;
    }

    public int getGiftCharm() {
        return giftCharm;
    }

    public void setGiftCharm(int giftCharm) {
        this.giftCharm = giftCharm;
    }

    public int getGiftRich() {
        return giftRich;
    }

    public void setGiftRich(int giftRich) {
        this.giftRich = giftRich;
    }

    @Override
    public String toString() {
        return "UserGift [uid=" + uid + ", giftPic=" + giftPic + ", giftName="
                + giftName + ", giftCharm=" + giftCharm + ", giftRich="
                + giftRich + ", addTime=" + addTime + ", sendName=" + sendName
                + "]";
    }

}
