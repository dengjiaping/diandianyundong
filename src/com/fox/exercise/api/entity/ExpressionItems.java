package com.fox.exercise.api.entity;


public class ExpressionItems {

    /**
     * 表情图片实体类
     */
    private int id;
    private String des;
    private String imgUrl;
    private Boolean isCanel;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return des;
    }

    public void setName(String name) {
        this.des = name;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public Boolean getIsCanel() {
        return isCanel;
    }

    public void setIsCanel(Boolean isCanel) {
        this.isCanel = isCanel;
    }
}
