package com.fox.exercise.newversion.entity;

import java.io.Serializable;

/**
 * @author loujungang 动态的评论bean
 */
public class SportCircleComments implements Serializable {
    private String id;//评论id
    private String uid;//评论者的id
    private String content;//回复内容
    private String wav;//语音地址
    private String wavtime;//语音时长,以秒为单位
    private String inputtime;//评论时间
    private String name;//评论者的网名
    private String img;//评论者的头像
    private String to_name;//回复评论者的网名
    private String to_img;//回复评论者的头像
    private String sex;//评论性别
    private String to_sex;//回复评论性别

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getWav() {
        return wav;
    }

    public void setWav(String wav) {
        this.wav = wav;
    }

    public String getWavtime() {
        return wavtime;
    }

    public void setWavtime(String wavtime) {
        this.wavtime = wavtime;
    }

    public String getInputtime() {
        return inputtime;
    }

    public void setInputtime(String inputtime) {
        this.inputtime = inputtime;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTo_name() {
        return to_name;
    }

    public void setTo_name(String to_name) {
        this.to_name = to_name;
    }

    public String getTo_img() {
        return to_img;
    }

    public void setTo_img(String to_img) {
        this.to_img = to_img;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getTo_sex() {
        return to_sex;
    }

    public void setTo_sex(String to_sex) {
        this.to_sex = to_sex;
    }


}
