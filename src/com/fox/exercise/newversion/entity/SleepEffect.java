package com.fox.exercise.newversion.entity;

import java.io.Serializable;

/**
 * @author loujungang  睡眠质量bean
 */
public class SleepEffect implements Serializable {
    private String uid;//用户id
    private String starttime;//开始时间
    private String endtime;//结束时间
    private String hart_rate;//睡眠质量百分比
    private String unique_id;//数据的唯一标识
    private String shock_num;//振动次数
    private String bright_time;//亮屏时常

    public String getShock_num() {
        return shock_num;
    }

    public void setShock_num(String shock_num) {
        this.shock_num = shock_num;
    }

    public String getBright_time() {
        return bright_time;
    }

    public void setBright_time(String bright_time) {
        this.bright_time = bright_time;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getStarttime() {
        return starttime;
    }

    public void setStarttime(String starttime) {
        this.starttime = starttime;
    }

    public String getEndtime() {
        return endtime;
    }

    public void setEndtime(String endtime) {
        this.endtime = endtime;
    }

    public String getHart_rate() {
        return hart_rate;
    }

    public void setHart_rate(String hart_rate) {
        this.hart_rate = hart_rate;
    }

    public String getUnique_id() {
        return unique_id;
    }

    public void setUnique_id(String unique_id) {
        this.unique_id = unique_id;
    }

}
