package com.fox.exercise.api.entity;

import java.util.ArrayList;

public class SquareInfo {

    public int id;
    public String cat_name;
    public int cat_order;
    public int typeid;
    public String url;
    public int actnum;
    private ArrayList<ActionInfos> actinfoList;//actnum不为0则有否则该数组没有数据


    public ArrayList<ActionInfos> getActinfoList() {
        return actinfoList;
    }

    public void setActinfoList(ArrayList<ActionInfos> actinfoList) {
        this.actinfoList = actinfoList;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCat_name() {
        return cat_name;
    }

    public void setCat_name(String cat_name) {
        this.cat_name = cat_name;
    }

    public int getCat_order() {
        return cat_order;
    }

    public void setCat_order(int cat_order) {
        this.cat_order = cat_order;
    }

    public int getTypeid() {
        return typeid;
    }

    public void setTypeid(int typeid) {
        this.typeid = typeid;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getActnum() {
        return actnum;
    }

    public void setActnum(int actnum) {
        this.actnum = actnum;
    }

    @Override
    public String toString() {
        return "SquareInfo [id=" + id + ", cat_name=" + cat_name
                + ", cat_order=" + cat_order + ", typeid=" + typeid + ", url="
                + url + ", actnum=" + actnum + "]";
    }

}
