package com.fox.exercise.newversion.entity;

import java.io.Serializable;

/**
 * @author loujungang 点赞用户实体类
 */
public class SportsLike implements Serializable {
    private int find_id;//	说说id
    private int uid;//	用户id
    private String name;//用户名字
    private String img;//	图片
    private int sex;//性别1 man; 其他 woman
    private int If_fans;//	是否是好友1标示是2标示不是
    private String time;//	点赞时间
    private SportRecord sportsdata;//运动的情况大数组  运动距离 运动时间

    public int getFind_id() {
        return find_id;
    }

    public void setFind_id(int find_id) {
        this.find_id = find_id;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public int getSex() {
        return sex;
    }

    public void setSex(int sex) {
        this.sex = sex;
    }

    public int getIf_fans() {
        return If_fans;
    }

    public void setIf_fans(int if_fans) {
        If_fans = if_fans;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public SportRecord getSportsdata() {
        return sportsdata;
    }

    public void setSportsdata(SportRecord sportsdata) {
        this.sportsdata = sportsdata;
    }


}
