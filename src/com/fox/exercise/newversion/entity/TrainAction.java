package com.fox.exercise.newversion.entity;

import java.io.Serializable;

/**
 * @author loujungang 该训练的训练动作
 */
public class TrainAction implements Serializable {
    private int id;//动作id
    private String name;//动作名称
    private String thumb;//动作图片地址
    private int listorder;//动作顺序
    private int resttime;//休息时长
    private int actnum;//动作次数
    private int videotime;//视频时长

    public int getResttime() {
        return resttime;
    }

    public void setResttime(int resttime) {
        this.resttime = resttime;
    }

    public int getActnum() {
        return actnum;
    }

    public void setActnum(int actnum) {
        this.actnum = actnum;
    }

    public int getVideotime() {
        return videotime;
    }

    public void setVideotime(int videotime) {
        this.videotime = videotime;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getThumb() {
        return thumb;
    }

    public void setThumb(String thumb) {
        this.thumb = thumb;
    }

    public int getListorder() {
        return listorder;
    }

    public void setListorder(int listorder) {
        this.listorder = listorder;
    }


}
