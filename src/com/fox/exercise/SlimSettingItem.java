package com.fox.exercise;

public class SlimSettingItem {
    private String name;
    private int flag;

    public SlimSettingItem(String name, int flag) {
        super();
        this.name = name;
        this.flag = flag;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getFlag() {
        return flag;
    }

    public void setFlag(int flag) {
        this.flag = flag;
    }


}
